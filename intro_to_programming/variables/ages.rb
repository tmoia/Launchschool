#Write a program called age.rb that asks a user
#how old they are and then tells them how old they
#will be in 10, 20, 30 and 40 years.
#Below is the output for someone 20 years old.

puts "how old are you?"
years = gets.chomp.to_i

puts "In 10 years old you will be: "
puts years + 10

puts "In 20 years old you will be: "
puts years + 20

puts "In 30 years old you will be: "
puts years + 30

puts "In 40 years old you will be: "
puts years + 40


